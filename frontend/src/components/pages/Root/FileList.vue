<template>
    <v-card>
        <v-list two-line subheader>
            <v-subheader inset>Файлы</v-subheader>
            <template v-for="(item, index) in files">
                <v-list-tile
                        :key="item.id"
                        avatar
                        @click=""
                >
                    <v-list-tile-avatar>
                        <v-icon class="grey white--text">{{ item.fileType }}</v-icon>
                    </v-list-tile-avatar>

                    <v-list-tile-content>
                        <v-list-tile-title>{{ item.file }}</v-list-tile-title>
                        <v-list-tile-sub-title class="text--primary">
                            Файл изменен: {{dateModified(item.modified) }}
                        </v-list-tile-sub-title>
                        <v-list-tile-sub-title v-if="item.secureLink">
                            Ссылка действительна до:
                        </v-list-tile-sub-title>
                    </v-list-tile-content>

                    <div v-if="item.secureLink">
                        <v-btn
                                color="success"
                                v-clipboard:copy="item.secureLink.url"
                        >Скопировать ссылку
                        </v-btn>
                    </div>

                    <v-btn v-else
                           color="primary"
                           @click="createLink(item)"
                    >Создать ссылку
                    </v-btn>

                    <file-list-menu :item="item"></file-list-menu>

                    <v-list-tile-action>
                        <v-list-tile-action-text>
                            {{humanFileSize(item.size, true)}}
                        </v-list-tile-action-text>
                    </v-list-tile-action>
                </v-list-tile>
                <v-divider
                        v-if="index + 1 < files.length"
                        :key="index"
                ></v-divider>

            </template>
        </v-list>
    </v-card>
</template>

<script>
    import FileListMenu from "./FileListMenu";

    export default {
        name: "FileList",
        props: ['files',],
        components: {FileListMenu},
        data() {
            return {}
        },
        methods: {
            dateModified(timestamp) {
                const moment = require('moment');
                let date = moment(timestamp * 1000);
                return date.locale("ru").format("DD MMM YYYY hh:mm")
            },
            humanFileSize(bytes, si) {
                let thresh = si ? 1000 : 1024;
                if (Math.abs(bytes) < thresh) {
                    return bytes + ' B';
                }
                let units = si
                    ? ['kB', 'MB', 'GB', 'TB', 'PB', 'EB', 'ZB', 'YB']
                    : ['KiB', 'MiB', 'GiB', 'TiB', 'PiB', 'EiB', 'ZiB', 'YiB'];
                let u = -1;
                do {
                    bytes /= thresh;
                    ++u;
                } while (Math.abs(bytes) >= thresh && u < units.length - 1);
                return bytes.toFixed(1) + ' ' + units[u];
            },
            createLink(file) {
                console.log(file)
            },
        }
    }
</script>